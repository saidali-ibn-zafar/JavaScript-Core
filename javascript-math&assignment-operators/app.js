var a =  1+2+3+4+5;
document.write(a,"\n");


// + is addition 
// - is subtraction
// / is division
// * is multiplication
// % is remainder
// and more ...


// Increment 
var chop = 7;
chop = chop + 1;
// or you can do 
chop++;
document.write(chop,"\n");

// Decrement
chop--
document.write(chop,"\n");



// ASSIGNMENT OPERATORS
var oldLevel = 5;
oldLevel += 13;
// we can also use other operators -,/,%,....
oldLevel -= 10;
oldLevel *= 2;
document.write(oldLevel,"\n");

